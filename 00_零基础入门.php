// 想象你是一个厨师，编程就是给计算机写"菜谱"。
// 你告诉计算机：先做什么，再做什么，遇到什么情况怎么办。
// 计算机就会严格按照你的"菜谱"执行，不会偷懒，不会犯错（除非你写错了）。

echo "【第一节：什么是编程？】\n\n";

// 编程的本质：用计算机能理解的语言，告诉它该做什么。
// 就像你用中文和朋友交流，编程就是用"编程语言"和计算机交流。

// 生活类比：
// 你每天早上起床的流程：
// 1. 闹钟响了 → 2. 关掉闹钟 → 3. 起床 → 4. 刷牙 → 5. 洗脸 → 6. 吃早餐
// 这就是一个"程序"！你把步骤写下来，就是"编程"。

// 编程语言有很多种：Python、Java、C++、JavaScript、PHP...
// 它们就像不同的方言，目的都是和计算机交流。

echo "编程 = 用计算机能理解的语言，告诉它该做什么\n";
echo "就像给计算机写一份详细的'菜谱'\n\n";


// =============================================
// 第二节：什么是PHP？
// =============================================
// PHP = PHP: Hypertext Preprocessor（超文本预处理器）
// 这是一个递归缩写，不用记，知道就好。

// PHP是什么？
// - 一种专门用于Web开发的编程语言
// - 世界上80%的网站都用PHP（包括Facebook、WordPress、Wikipedia）
// - 它可以嵌入HTML中，在服务器上运行

// 生活类比：
// 想象一个餐厅：
// - 前端（HTML/CSS/JavaScript）= 餐厅的装修、菜单、服务员（你看到的）
// - 后端（PHP/Python/Java）= 厨房里的厨师（你看不到的，但做出菜来）
// - 数据库（MySQL）= 冰箱和食材库（存储所有数据）

// PHP的工作流程：
// 1. 用户在浏览器输入网址
// 2. 浏览器发送请求到服务器
// 3. 服务器上的PHP代码开始执行
// 4. PHP生成HTML页面
// 5. 服务器把HTML发回浏览器
// 6. 浏览器显示页面

echo "【第二节：什么是PHP？】\n\n";
echo "PHP = 超文本预处理器，专门用于Web开发\n";
echo "世界80%的网站都用PHP（Facebook、WordPress等）\n\n";


// =============================================
// 第三节：第一个PHP程序
// =============================================
// 传统上，学任何编程语言的第一个程序都是输出 "Hello, World!"
// 这个传统从1978年就开始了，由C语言的发明者Brian Kernighan提出。

echo "【第三节：第一个PHP程序】\n\n";

// 这就是最简单的PHP程序
echo "Hello, World!\n";
// echo 是PHP的"输出"命令，就像你对着计算机说"把这个显示出来"
// "Hello, World!" 是要显示的内容
// 分号 ; 表示这句话说完了（就像中文的句号）

// 你可以输出任何内容
echo "你好，世界！\n";
echo "我正在学习PHP！\n";
echo "今天是学习编程的第一天！\n\n";

// 多行输出
echo "PHP程序的基本结构：\n";
echo "1. 以 <?php 开头\n";
echo "2. 写你的代码\n";
echo "3. 每条语句以分号结尾\n";
echo "4. 用 echo 输出内容\n\n";


// =============================================
// 第四节：变量初体验 —— 编程的"盒子"
// =============================================
// 变量就像盒子，用来存放数据。详细规则在第01课讲解。

echo "【第四节：变量初体验】\n\n";

// 创建变量：$变量名 = 值
$name = "小明";        // 字符串
$age = 18;             // 整数
$height = 175.5;       // 浮点数
$is_student = true;    // 布尔值

echo "姓名：$name，年龄：$age，身高：$height\n";

// 变量可以改变
$age = 19;
echo "过了一年，年龄变成：$age\n";

// 变量可以参与计算
echo "出生年份（大约）：" . (2024 - $age) . "\n\n";


// =============================================
// 第五节：数据类型速览 —— 盒子里装的是什么？
// =============================================
// PHP有几种基本数据类型，第01课会详细讲解。

echo "【第五节：数据类型速览】\n\n";

// 1. 字符串（string）—— 一串文字
echo "字符串：\"你好\"、'小红'\n";

// 2. 整数（integer）—— 没有小数点的数字
echo "整数：95、100、-10\n";

// 3. 浮点数（float）—— 有小数点的数字
echo "浮点数：9.99、3.14159\n";

// 4. 布尔值（boolean）—— 只有 true 或 false
echo "布尔值：true（真）、false（假）\n";

// 5. 数组（array）—— 一组数据
$fruits = ["苹果", "香蕉", "橙子"];
echo "数组：";
print_r($fruits);

// 6. NULL —— 空值，什么都没有
echo "NULL：表示\"没有值\"\n\n";

// 用 var_dump() 可以查看变量的详细类型信息
echo "用 var_dump() 查看类型：\n";
var_dump($name);       // string(6) "小明"
var_dump($age);        // int(19)
var_dump($height);     // float(175.5)
echo "\n";


// =============================================
// 第六节：简单计算 —— 让PHP当计算器
// =============================================
// 运算符的详细讲解在第02课，这里先体验一下。

echo "【第六节：简单计算】\n\n";

// PHP可以做加减乘除
$a = 10;
$b = 3;

echo "$a + $b = " . ($a + $b) . "\n";   // 加法：13
echo "$a - $b = " . ($a - $b) . "\n";   // 减法：7
echo "$a * $b = " . ($a * $b) . "\n";   // 乘法：30
echo "$a / $b = " . ($a / $b) . "\n";   // 除法：3.333...

echo "\n";

// 字符串拼接：用点号 .
echo "字符串拼接：" . ("张" . "三") . "\n\n";


// =============================================
// 第七节：输出格式化 —— 让输出更美观
// =============================================

echo "【第七节：输出格式化】\n\n";

// 使用双引号可以直接嵌入变量
$city = "北京";
$temperature = 28;
echo "今天$city的温度是{$temperature}度\n";

// 使用 printf 格式化输出
$name3 = "小明";
$score2 = 95.678;
printf("%s的成绩是%.2f分\n", $name3, $score2);
// %s = 字符串，%.2f = 保留2位小数的浮点数

// 换行符 \n 和制表符 \t
echo "第一行\n第二行\n第三行\n";
echo "列1\t列2\t列3\n";
echo "数据\t数据\t数据\n\n";

// 多行输出用 heredoc 语法
$message = <<<EOT
这是一个多行文本：
第一行内容
第二行内容
第三行内容
变量可以嵌入：$name3
EOT;
echo $message . "\n\n";


