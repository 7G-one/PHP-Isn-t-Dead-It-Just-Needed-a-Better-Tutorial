echo "练习5：创建一个简单的权限系统：\n";
echo "   - 定义权限常量：READ=1, WRITE=2, EXECUTE=4\n";
echo "   - 给用户添加 READ 和 WRITE 权限\n";
echo "   - 检查用户是否有 EXECUTE 权限\n";
echo "  提示：添加权限用按位或 |，检查权限用按位与 & !== 0\n";
echo "  提示：每种权限占一个二进制位：001、010、100\n\n";


