<?php
// =============================================
// 第 02 课：运算符与表达式
// =============================================
// 上节课我们学了变量与数据类型。
// 这节课我们要学习各种运算符，让PHP真正"计算"起来。

echo "============================================================\n";
echo "   第02课：运算符与表达式\n";
echo "============================================================\n\n";


// =============================================
// 第一节：算术运算符
// =============================================
// 就像你用计算器一样，PHP可以做各种数学运算。

echo "【第一节：算术运算符】\n\n";

$a = 15;
$b = 4;

echo "a = $a, b = $b\n\n";

// 加法
echo "加法：$a + $b = " . ($a + $b) . "\n";  // 19

// 减法
echo "减法：$a - $b = " . ($a - $b) . "\n";  // 11

// 乘法
echo "乘法：$a * $b = " . ($a * $b) . "\n";  // 60

// 除法
echo "除法：$a / $b = " . ($a / $b) . "\n";  // 3.75

// 取模（取余数）
echo "取模：$a % $b = " . ($a % $b) . "\n";  // 3（15 ÷ 4 = 3 余 3）

// 幂运算（PHP 5.6+）
echo "幂运算：$a ** $b = " . ($a ** $b) . "\n";  // 50625（15的4次方）

// 整除（PHP 7.0+）
echo "整除：intdiv($a, $b) = " . intdiv($a, $b) . "\n";  // 3

echo "\n";

// 实际应用：计算圆的面积
$radius = 5;
$pi = 3.14159;
$area = $pi * $radius ** 2;  // π × r²
$perimeter = 2 * $pi * $radius;  // 2 × π × r
echo "圆的半径：$radius\n";
echo "面积：$area\n";
echo "周长：$perimeter\n\n";


// =============================================
// 第二节：赋值运算符
// =============================================
// 赋值就是"把值放进盒子里"。

echo "【第二节：赋值运算符】\n\n";

// 基本赋值
$x = 10;
echo "基本赋值：x = $x\n";

// 加法赋值
$x += 5;  // 等价于 $x = $x + 5
echo "x += 5 后：x = $x\n";  // 15

// 减法赋值
$x -= 3;  // 等价于 $x = $x - 3
echo "x -= 3 后：x = $x\n";  // 12

// 乘法赋值
$x *= 2;  // 等价于 $x = $x * 2
echo "x *= 2 后：x = $x\n";  // 24

// 除法赋值
$x /= 4;  // 等价于 $x = $x / 4
echo "x /= 4 后：x = $x\n";  // 6

// 取模赋值
$x %= 4;  // 等价于 $x = $x % 4
echo "x %= 4 后：x = $x\n";  // 2

// 幂赋值
$x **= 3;  // 等价于 $x = $x ** 3
echo "x **= 3 后：x = $x\n";  // 8

// 字符串拼接赋值
$str = "Hello";
$str .= " World";  // 等价于 $str = $str . " World"
echo "字符串拼接赋值：$str\n\n";

// 链式赋值
$a = $b = $c = 100;
echo "链式赋值：a=$a, b=$b, c=$c\n\n";


// =============================================
// 第三节：比较运算符
// =============================================
// 比较运算符用来比较两个值，返回 true 或 false。
// 就像你问"苹果和橘子哪个大？"，答案是"是"或"否"。

echo "【第三节：比较运算符】\n\n";

$a = 10;
$b = "10";
$c = 20;

echo "a = $a (整数), b = \"$b\" (字符串), c = $c\n\n";

// 等于（值相等，类型可以不同）
echo "等于：a == b → " . var_export($a == $b, true) . "\n";  // true

// 全等（值和类型都要相等）
echo "全等：a === b → " . var_export($a === $b, true) . "\n";  // false
echo "全等：a === 10 → " . var_export($a === 10, true) . "\n";  // true

// 不等于
echo "不等于：a != c → " . var_export($a != $c, true) . "\n";  // true
echo "不等于：a <> c → " . var_export($a <> $c, true) . "\n";  // true（另一种写法）

// 不全等
echo "不全等：a !== b → " . var_export($a !== $b, true) . "\n";  // true

// 大于
echo "大于：c > a → " . var_export($c > $a, true) . "\n";  // true

// 小于
echo "小于：a < c → " . var_export($a < $c, true) . "\n";  // true

// 大于等于
echo "大于等于：a >= 10 → " . var_export($a >= 10, true) . "\n";  // true

// 小于等于
echo "小于等于：a <= 5 → " . var_export($a <= 5, true) . "\n";  // false

echo "\n";

// 太空船运算符（PHP 7.0+）
// 返回 -1（小于）、0（等于）、1（大于）
echo "太空船运算符：\n";
echo "10 <=> 20 = " . (10 <=> 20) . "\n";  // -1
echo "20 <=> 20 = " . (20 <=> 20) . "\n";  // 0
echo "30 <=> 20 = " . (30 <=> 20) . "\n";  // 1

// 字符串比较
echo "\n字符串比较（按字典顺序）：\n";
echo "\"apple\" < \"banana\" → " . var_export("apple" < "banana", true) . "\n";  // true
echo "\"abc\" < \"abd\" → " . var_export("abc" < "abd", true) . "\n";  // true

// NULL 合并运算符（PHP 7.0+）
echo "\nNULL 合并运算符（??）：\n";
// 注意：$_GET 在第 8 课讲解，这里先用变量演示 ?? 运算符
// $username = $_GET['user'] ?? '访客';  // 原始写法（第 8 课再学）
$unknown = null;
$username = $unknown ?? '访客';  // null 时用默认值
echo "用户名：$username\n";
$username = '小明' ?? '访客';  // 有值时直接用
echo "用户名：$username\n\n";


// =============================================
// 第四节：逻辑运算符
// =============================================
// 逻辑运算符用来组合多个条件，就像日常生活中的"与""或""非"。

echo "【第四节：逻辑运算符】\n\n";

$age = 25;
$has_ticket = true;
$is_vip = false;

echo "年龄：$age，有票：" . ($has_ticket ? '是' : '否') . "，VIP：" . ($is_vip ? '是' : '否') . "\n\n";

// 逻辑与（AND / &&）—— 两个条件都满足
$can_enter = $age >= 18 && $has_ticket;
echo "年龄>=18 并且 有票 → 可以进入：" . ($can_enter ? '是' : '否') . "\n";

// 逻辑或（OR / ||）—— 至少一个条件满足
$is_special = $has_ticket || $is_vip;
echo "有票 或者 是VIP → 特殊身份：" . ($is_special ? '是' : '否') . "\n";

// 逻辑非（NOT / !）—— 取反
$is_minor = !($age >= 18);
echo "不是成年人：" . ($is_minor ? '是' : '否') . "\n";

// 异或（XOR）—— 只有一个条件满足
$either = $has_ticket xor $is_vip;
echo "有票 异或 是VIP：" . ($either ? '是' : '否') . "\n\n";

// AND 和 && 的区别
echo "AND 和 && 的区别：\n";
echo "&& 优先级高于 =，AND 优先级低于 =\n";
echo "实际开发中，建议使用 && 和 ||\n\n";

// 短路求值
echo "短路求值：\n";
echo "如果第一个条件已经确定结果，第二个条件不会执行\n";
// 示例：如果 $x 不为 null，才访问 $x->method()
// $result = $x !== null && $x->method();
// 这样可以避免空指针错误


// =============================================
// 第五节：位运算符
// =============================================
// 位运算符直接操作二进制位，就像在玩"开关游戏"。
// 每一位就像一个开关，0是关，1是开。

echo "【第五节：位运算符】\n\n";

$a = 12;  // 二进制：1100
$b = 10;  // 二进制：1010

echo "a = $a (二进制: " . decbin($a) . ")\n";
echo "b = $b (二进制: " . decbin($b) . ")\n\n";

// 按位与（AND）—— 两个都是1才是1
$result = $a & $b;
echo "按位与：$a & $b = $result (二进制: " . decbin($result) . ")\n";

// 按位或（OR）—— 至少一个是1就是1
$result = $a | $b;
echo "按位或：$a | $b = $result (二进制: " . decbin($result) . ")\n";

// 按位异或（XOR）—— 不同才是1
$result = $a ^ $b;
echo "按位异或：$a ^ $b = $result (二进制: " . decbin($result) . ")\n";

// 按位取反（NOT）—— 0变1，1变0
$result = ~$a;
echo "按位取反：~$a = $result (二进制: " . decbin($result) . ")\n";

// 左移（乘以2的n次方）
$result = $a << 2;
echo "左移2位：$a << 2 = $result (相当于 $a * 4)\n";

// 右移（除以2的n次方）
$result = $a >> 1;
echo "右移1位：$a >> 1 = $result (相当于 $a / 2)\n\n";

// 实际应用：权限系统
echo "位运算实际应用 - 权限系统：\n";
define("PERMISSION_READ", 1);    // 0001
define("PERMISSION_WRITE", 2);   // 0010
define("PERMISSION_DELETE", 4);  // 0100
define("PERMISSION_ADMIN", 8);   // 1000

$user_permission = PERMISSION_READ | PERMISSION_WRITE;  // 0011（读+写）
echo "用户权限：" . decbin($user_permission) . "\n";

// 检查是否有读权限
$has_read = ($user_permission & PERMISSION_READ) !== 0;
echo "有读权限：" . ($has_read ? '是' : '否') . "\n";

// 检查是否有删除权限
$has_delete = ($user_permission & PERMISSION_DELETE) !== 0;
echo "有删除权限：" . ($has_delete ? '是' : '否') . "\n";

// 添加删除权限
$user_permission |= PERMISSION_DELETE;
echo "添加删除权限后：" . decbin($user_permission) . "\n";

// 移除删除权限
$user_permission &= ~PERMISSION_DELETE;
echo "移除删除权限后：" . decbin($user_permission) . "\n\n";


// =============================================
// 第六节：其他运算符
// =============================================

echo "【第六节：其他运算符】\n\n";

// 三元运算符
$age = 20;
$status = ($age >= 18) ? "成年人" : "未成年人";
echo "三元运算符：$age岁 → $status\n";

// 空合并运算符（PHP 7.0+）
$name = null;
$display_name = $name ?? "匿名用户";
echo "空合并运算符：$display_name\n";

// 太空船运算符（PHP 7.0+）
echo "太空船运算符：\n";
echo "5 <=> 3 = " . (5 <=> 3) . "\n";   // 1
echo "3 <=> 5 = " . (3 <=> 5) . "\n";   // -1
echo "5 <=> 5 = " . (5 <=> 5) . "\n\n"; // 0

// 执行运算符（反引号）
// $result = `dir`;  // 执行系统命令
// echo $result;

// 类型运算符
echo "类型运算符 instanceof：\n";
class MyClass {}
$obj = new MyClass();
echo "\$obj 是 MyClass 的实例：" . ($obj instanceof MyClass ? '是' : '否') . "\n\n";


// =============================================
// 第七节：运算符优先级
// =============================================
// 就像数学中的"先乘除后加减"，PHP运算符也有优先级。

echo "【第七节：运算符优先级】\n\n";

echo "运算符优先级（从高到低）：\n";
echo "1. () 括号（最高优先级）\n";
echo "2. ** 幂运算\n";
echo "3. ++ -- 自增自减\n";
echo "4. ~ ! 类型转换、取反\n";
echo "5. * / % 乘除取模\n";
echo "6. + - . 加减字符串连接\n";
echo "7. << >> 位移\n";
echo "8. < <= > >= 比较\n";
echo "9. == != === !== 等于\n";
echo "10. & 按位与\n";
echo "11. ^ 按位异或\n";
echo "12. | 按位或\n";
echo "13. && 逻辑与\n";
echo "14. || 逻辑或\n";
echo "15. ?? 空合并\n";
echo "16. ? : 三元\n";
echo "17. = += -= 等赋值\n";
echo "18. and 逻辑与\n";
echo "19. xor 逻辑异或\n";
echo "20. or 逻辑或（最低优先级）\n\n";

// 实际例子
echo "优先级示例：\n";
echo "2 + 3 * 4 = " . (2 + 3 * 4) . "\n";      // 14，不是20
echo "(2 + 3) * 4 = " . ((2 + 3) * 4) . "\n";  // 20

echo "\n建议：不确定优先级时，用括号明确表达！\n\n";


// =============================================
// 第八节：自增自减运算符
// =============================================

echo "【第八节：自增自减运算符】\n\n";

$x = 5;
echo "初始值：x = $x\n";

// 后置自增：先使用，再增加
echo "x++ = " . ($x++) . "（先输出，再增加）\n";
echo "现在 x = $x\n\n";

// 前置自增：先增加，再使用
echo "++x = " . (++$x) . "（先增加，再输出）\n";
echo "现在 x = $x\n\n";

// 后置自减
echo "x-- = " . ($x--) . "（先输出，再减少）\n";
echo "现在 x = $x\n\n";

// 前置自减
echo "--x = " . (--$x) . "（先减少，再输出）\n";
echo "现在 x = $x\n\n";


