echo "  提示：记住优先级——括号 > 幂 > 乘除 > 加减，一步步算\n\n";

echo "练习2：预测以下代码的输出：\n";
echo "   \$a = 5;\n";
echo "   \$b = \$a++ + ++\$a;\n";
echo "   echo \$b;\n";
echo "  提示：\$a++ 先返回值再自增，++\$a 先自增再返回值，分两步思考\n\n";

echo "练习3：使用三元运算符，判断一个数字是奇数还是偶数\n";
echo "  提示：用取模运算 \$num % 2，余数为0是偶数，否则是奇数\n";
echo "  提示：三元运算符格式—— (条件) ? \"偶数\" : \"奇数\"\n\n";

echo "练习4：使用位运算，交换两个变量的值（不用第三个变量）\n";
